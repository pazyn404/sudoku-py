import pytest

from sudoku.sudoku import Sudoku

grid = [
    [0, 4, 0, 0, 3, 0, 8, 0, 7],
    [0, 0, 0, 0, 0, 4, 0, 0, 5],
    [0, 0, 7, 0, 1, 0, 9, 0, 0],
    [0, 0, 0, 6, 0, 0, 0, 5, 9],
    [0, 0, 0, 0, 9, 0, 0, 0, 0],
    [4, 2, 0, 0, 0, 8, 0, 0, 0],
    [0, 0, 8, 0, 7, 0, 4, 0, 0],
    [6, 0, 0, 4, 0, 0, 0, 0, 0],
    [7, 0, 4, 0, 8, 0, 0, 1, 0],
]

solution = [
    [1, 4, 6, 5, 3, 9, 8, 2, 7],
    [9, 8, 2, 7, 6, 4, 1, 3, 5],
    [3, 5, 7, 8, 1, 2, 9, 4, 6],
    [8, 7, 3, 6, 4, 1, 2, 5, 9],
    [5, 6, 1, 2, 9, 7, 3, 8, 4],
    [4, 2, 9, 3, 5, 8, 6, 7, 1],
    [2, 9, 8, 1, 7, 5, 4, 6, 3],
    [6, 1, 5, 4, 2, 3, 7, 9, 8],
    [7, 3, 4, 9, 8, 6, 5, 1, 2],
]


@pytest.mark.parametrize("grid,solution", [(grid, solution)])
def test_eval(grid, solution):
    s = Sudoku(grid)
    s.solve()
    assert s.solutions[0] == solution
